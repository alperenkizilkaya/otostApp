package com.example.otostapp;

public class FilmGenre {
    public String genre;
    public long id;

    public void setGenre(String genre) {
        this.genre = genre;
    }

    public void setId(long id) {
        this.id = id;
    }

    public String getGenre()
    {
        return genre;
    }

    public long getId()
    {
        return id;
    }
}
